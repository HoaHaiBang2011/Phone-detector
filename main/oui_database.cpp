const uint32_t OUIDatabase::oui_table_size = sizeof(oui_table) / sizeof(OUI_Entry_t);

const char* OUIDatabase::lookupManufacturer(const uint8_t* mac_oui) {
    if (!mac_oui) return "Unknown";
    
    for (uint32_t i = 0; i < oui_table_size; i++) {
        if (mac_oui[0] == oui_table[i].oui[0] &&
            mac_oui[1] == oui_table[i].oui[1] &&
            mac_oui[2] == oui_table[i].oui[2]) {
            return oui_table[i].manufacturer;
        }
    }
    return "Unknown";
}

uint8_t OUIDatabase::getDeviceCount(const char* manufacturer) {
    uint8_t count = 0;
    for (uint32_t i = 0; i < oui_table_size; i++) {
        if (strcmp(oui_table[i].manufacturer, manufacturer) == 0) {
            count++;
        }
    }
    return count;
}
