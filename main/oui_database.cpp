const uint32_t OUIDatabase::oui_table_size = sizeof(oui_table) / sizeof(OUI_Entry_t);

const char* OUIDatabase::lookupManufacturer(const uint8_t* mac) {
    if (!mac) return "Unknown";
    
    for (uint32_t i = 0; i < oui_table_size; i++) {
        // Đọc từng byte OUI từ Flash để so sánh
        if (mac[0] == pgm_read_byte(&(oui_table[i].oui[0])) &&
            mac[1] == pgm_read_byte(&(oui_table[i].oui[1])) &&
            mac[2] == pgm_read_byte(&(oui_table[i].oui[2]))) {
            // Trả về con trỏ chuỗi nằm trong Flash
            return (const char*)pgm_read_ptr(&(oui_table[i].manufacturer));
        }
    }
    return "Unknown";
}
